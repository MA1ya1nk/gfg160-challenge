class Solution {
  public:
    // Function to find the length of a loop in the linked list.
    int countNodesinLoop(Node *head) {
        // Code here
        unordered_map<Node*,pair<bool,int>> check;
        Node* temp=head;
        Node* prev=NULL;
        bool loop=false;
        int size=0;
        while(temp != NULL){
            if(check[temp].first){
                loop=true;
                break;
            }
            check[temp].first=true;
            check[temp].second=size++;
            prev=temp;
            temp=temp->next;
        }
        
        return loop? check[prev].second-check[temp].second+1 : 0;
    }
};


